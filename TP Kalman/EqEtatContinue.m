% Equation d'état continue, non linéaire
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function[dotX]=EqEtatContinue(t,X,Param,u)

%------------------------------------------------------------------------
%Mise en forme pour traitement plus pratique
x2=X(2,1);
%------------------------------------------------------------------------
x1=X(1,1);
%  *****   A COMPLETER
%------------------------------------------------------------------------
%expression de la dérivée de x1

dotx1= -x2*u + Param.beta;

%expression de la dérivée de x2
dotx2= -Param.alpha*x2^2+u*x2;
%------------------------------------------------------------------------

 dotX=[dotx1;dotx2];